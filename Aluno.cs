class Aluno
{
    private string Nome, Rgm, DataNascimento, Curso, Rg, Genero;
    private bool Bolsista;


    public string getNome()
    {
        return Nome;
    }

    public void setNome(string value)
    {
        Nome = value;
    }

    public string getRgm()
    {
        return Rgm;
    }

    public void setRgm(string value)
    {
        Rgm = value;
    }

    public string getDataNascimento()
    {
        return DataNascimento;
    }

    public void setDataNascimento(string value)
    {
        DataNascimento = value;
    }

    public string getCurso()
    {
        return Curso;
    }

    public void setCurso(string value)
    {
        Curso = value;
    }

    public string getRg()
    {
        return Rg;
    }

    public void setRg(string value)
    {
        Rg = value;
    }

    public string getGenero()
    {
        return Genero;
    }

    public void setGenero(string value)
    {
        Genero = value;
    }

    public bool getBolsista()
    {
        return Bolsista;
    }

    public void setBolsista(bool value)
    {
        Bolsista = value;
    }












}